void app_menu () {
  Serial.println("🟡 Entering app_menu");

  DIR* dir = opendir("/sdcard/apps");
  if (!dir) {
    clearGray(0);
    drawText(0, 10, "No /sdcard/apps", 15);
    sendToDisplay();
    delay(1500);
    return;
  }
  
  drawBitmap(0, 0, logo_bitmap, 256, 128, 15, true, 0);
  sendToDisplay();
  playWav("/sdcard/sound/startup.wav");

  std::vector<std::string> filenames;
  struct dirent* entry;
  while ((entry = readdir(dir)) != NULL) {
    // Можно отфильтровать только .bin и .app,
    // чтобы не показывать "." и ".."
    std::string name = entry->d_name;
    if (name == "." || name == "..") continue;

    // filter out any non-bin files
    if (name.size() < 4 || name.substr(name.size() - 4) != ".bin") continue;
    
    filenames.emplace_back(name);
  }
  closedir(dir);

  if (filenames.empty()) {
    clearGray(0);
    drawText(0, 10, "No apps found", 15);
    sendToDisplay();
    delay(1500);
    return;
  }
  clearGray(0);
  drawBitmap(0, 0, app_select_bitmap, 256, 128, 15, true, 0); 
  
  int selected = 0;
  unsigned long lastDraw = 0;

  while (true) {
    if (millis() - lastDraw > 100) {
      drawBox(0, 24, 128, 92, 0);
      for (int i = 0; i < (int)filenames.size(); ++i) {
        const int y = i * 12 + 34;  // чуть сдвинул для читабельности
        if (i == selected) {
          drawBox(0, i * 12 + 24, 128, 12, 15);                            // заливка
          drawText(4, y, filenames[i].c_str(), 0);                   // текст инвертированный
        } else {
          drawText(4, y, filenames[i].c_str(), 15);                   // обычный текст
        }
      }

      sendToDisplay();
      lastDraw = millis();
    }

    // ↓
    if (buttonPressed(5)) {
      selected = (selected + 1) % filenames.size();
      delay(150); // простейший антидребезг
    }

    // ↑
    if (buttonPressed(4)) {
      selected = (selected - 1 + filenames.size()) % filenames.size();
      delay(150);
    }

    // RIGHT → запустить
    if (buttonPressed(7)) {
      const char* name = filenames[selected].c_str();
      std::string full = "/sdcard/apps/" + std::string(name);

      if (strstr(name, ".bin")) {
        // теперь вызываем новую функцию
        run_binary_app(full.c_str());
        // если всё прошло успешно, дальше мы уже не вернёмся —
        // будет esp_restart() внутри run_binary_app
      } else if (strstr(name, ".app")) {
        // пока оставим старое поведение для .app
        playWav("/sdcard/test.wav");
        delay(300);
        // здесь позже можно будет прикрутить интерпретатор
      }

      break; // выходим из меню после попытки запуска
    }
  }
}

